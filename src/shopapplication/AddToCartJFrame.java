    public void increaseAmount(){
        increaseButton.addActionListener((ActionEvent e) -> {
            int increasedAmount = Integer.parseInt(amountTextField.getText()) + 1;
            amountTextField.setText(String.valueOf(increasedAmount));
        });
    }
    
    public void decreaseAmount(){
        decreaseButton.addActionListener((ActionEvent e) -> {
            if(Integer.parseInt(amountTextField.getText()) - 1 > 0){
            int decreasedAmount = Integer.parseInt(amountTextField.getText()) - 1;
            amountTextField.setText(String.valueOf(decreasedAmount));
            } else {
                System.out.println("The amount can't be less than 0!!");
            }
        });
    }
    
    public void addToCartByButton(Cart mainCart, Ware selectedWare, Storage mainStorage, JTable waresTable){
        addButton.addActionListener((ActionEvent e) -> {
        int amount = Integer.parseInt(amountTextField.getText());
        if(amount <= selectedWare.getQuantity()){
        Ware buyWare = new Ware(selectedWare.getName(), selectedWare.getPrice(), selectedWare.getCategory(), amount);
        mainCart.addToCart(buyWare);
        File cart = new File("cart.txt");
            try {
                FileWriter cartWriter = new FileWriter(cart);
                if(!mainCart.getCart().isEmpty()){
                for(int i = 0; i < mainCart.getCart().size(); i++){
                    cartWriter.write(mainCart.getCart().get(i).getName() + ";" + mainCart.getCart().get(i).getPrice() + ";" + mainCart.getCart().get(i).getCategory() + ";" + mainCart.getCart().get(i).getQuantity() + "\n");
                    }
                }
                cartWriter.close();
            } catch (IOException ex) {
                Logger.getLogger(AddToCartJFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        else{
                    System.out.println("Insufficient amount!");
        }
        });
    }

